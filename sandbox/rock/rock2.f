       external f
c *** *** *** *** *** *** *** *** *** *** *** *** *** *** *** 
c             Initialisations
c *** *** *** *** *** *** *** *** *** *** *** *** *** *** ***
      err=0.d0
      mz=mp(1)
      mr=mp(2)
c -------- First stage.--------
      temp1=h*recf(mr)
      ci1=t+temp1
      ci2=t+temp1
      ci3=t
      do i=1,neqn
        yjm2(i)=yn(i)
        yjm1(i)=yn(i)+temp1*fn(i)
        if (mdeg.lt.2) then
          y(i)=yjm1(i)
        end if
      end do
c --------- Stages for j=2..mdeg.--------
      do i=2,mdeg
        temp1=h*recf(mr+2*(i-2)+1)
        temp3=-recf(mr+2*(i-2)+2)
        temp2=1.d0-temp3
        call f(neqn,ci1,yjm1,y)
        ci1=temp1+temp2*ci2+temp3*ci3
        do j=1,neqn
          y(j)=temp1*y(j)+temp2*yjm1(j)+temp3*yjm2(j)
c -------- Shift the value "y" for the next stage.--------
          if (i.lt.mdeg) then
            yjm2(j)=yjm1(j)
            yjm1(j)=y(j)
          end if
        end do
        ci3=ci2
        ci2=ci1
      end do
c -------- The two-stage finishing procedure.--------
      temp1=h*fp1(mz)
      temp2=h*fp2(mz)
      call f(neqn,ci1,y,yjm2)
      do j=1,neqn
        yjm1(j)=y(j)+temp1*yjm2(j)
      end do
      ci1=ci1+temp1
      call f(neqn,ci1,yjm1,y)
c -------- Atol and rtol are scalar.--------
      if (ntol.eq.0) then
        ato=atol(1)
        rto=rtol(1)
        do j=1,neqn
          temp3=temp2*(y(j)-yjm2(j))
          y(j)=yjm1(j)+temp1*y(j)+temp3
          ci1=dmax1(dabs(y(j)),dabs(yn(j)))*rto
          err=err+(temp3/(ato+ci1))**2
        end do
c -------- Atol and rtol are array.--------
                     else
        do j=1,neqn
          temp3=temp2*(y(j)-yjm2(j))
          y(j)=yjm1(j)+temp1*y(j)+temp3
          ci1=dabs(y(j))*rtol(j)
          err=err+(temp3/(atol(j)+ci1))**2
        end do
      end if
      err=sqrt(err/neqn)
      return 
      end
c ----------------------------------------------
c     End of subroutine rtstep.
